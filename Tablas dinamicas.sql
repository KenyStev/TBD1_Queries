SELECT
  TO_CHAR(FECHA,'YYYY') AS ANIO,
  SUM(CASE WHEN CLIENTE = 'Cliente 4' THEN TOTAL ELSE NULL END) AS CLIENTE_4,
  SUM(CASE WHEN CLIENTE = 'Cliente 3' THEN TOTAL ELSE NULL END) AS CLIENTE_3,
  SUM(CASE WHEN CLIENTE = 'Cliente 2' THEN TOTAL ELSE NULL END) AS CLIENTE_2
FROM
  VENTAS
GROUP BY TO_CHAR(FECHA,'YYYY');